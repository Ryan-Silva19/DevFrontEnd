// 1 - Crie duas variáveis com idades e compare:
// - idade1 é maior que idade2?
// - Elas têm a mesma idade (usando ===)?  
// - As idades são diferentes?
let age = 19;
let age2 = 21;
console.log(`A idade um é maior que a idade 2 ?\n ${age > age2}`);
console.log(`As idades são parecidas ?\n ${age == age2}`);
console.log(`As idades são diferentes ?\n ${age != age2}`);
